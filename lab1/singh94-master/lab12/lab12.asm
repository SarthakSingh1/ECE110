; This program is designed to take any 3 inputs of ascii characters and create a bigger font of one of the ascii characters
; using the other 2 ascii characters
; To solve this problem we have to store these 3 inputs in registers and then properly traverse 16 addresses in "FONT_DATA" 
; To form our final picture. The design aproach here was to utilize loops and counters to loop through each bit in each adress
; Replacing 0's and 1's with their correct representation


;Register Table:
;R0: has multiple purposes, first it is used as a counter to do multiplication and later is used to output our 0's and 1's 
;R1 hold the value of our ascii value times 16
;R6 holds the value of FONT_DATA + R1
;R2 holds the actual data at the memeory address in R6
;R5 is the counter for the vertical addreses(Starts at 16)
;R4 is the counter for the horizantal data bit(Starts at 8 since we are only changing the first 8)
;R3  was just used to troubleshoot the output and make sure the correct value was being sent to R0 to print

.ORIG x3000 ; Start at x3000

	LDI R1, CHAR ; Loads the hexidecimal value of CHAR into R1
	

;this part multiplies our decimal value by 16 and keeps it in r1
		AND R0, R0, #0 ; 
		ADD R0, R0, #4 ;
LOOP1		ADD R1, R1, R1 ;
		ADD R0, R0, #-1 ; 
		BRp LOOP1 ; 
	
; Finds the memeory adress to begin with for each letter and loads the contents of it into a register R2
		LEA R6, FONT_DATA 	; The adress where font data starts is stored in R6
		ADD R6, R6, R1 		; add that adress to R1
		LDR R2, R6, #0		; Get the memory at the new address store it in R2

; Create a counter for the vertical columns 
		AND R5, R5, #0		;
		ADD R5, R5, #16      	; 

;Initializes a row and makes sure you havent done more than 16 rows
NEWVERT		ADD R5, R5, #0		; For the BR instruction
		BRz FINISH		; The program is done if your counter is 0
		AND R4, R4, #0		; R4 will be where the row number will be stored so it needs to be reset every time
		ADD R4, R4, #8		;
		LDR R2, R6, #0		; Loads the new address once it increments

; Travereses each memory address 
NEWHORI		ADD R4, R4, #0 		; This is here for the BR instruction to check is R0 is zero
		BRz NEXTROW		;
		AND R0, R0, #0		; We  are clearing R0 to use it as the value of the most  signifigant bit to print
		ADD R2, R2, #0		; For the BR istruction  
		BRn PRINTONE 		; if the  number is negative the most signifigant bit is 1
		AND R0, R0, #0		; If its not negative we need to  print 0
		LDI R3, ZERO		; R3 will contain the value to print
		ADD R0, R3, #0		; OUT will only print R0
		OUT			; 
		BRnzp  SHIFT		;   Do a left shift

; Prints the Text associated with 1		
PRINTONE	AND R0, R0, #0;
		LDI R3, ONE ;
		ADD R0, R3, #0;
		OUT ;

; Preform a  shift left to get a new signifigant bit
SHIFT 		ADD R2, R2, R2 		;  Shift Left
		ADD R4, R4, #-1		; Decrease the horizantal Counter
		BRnzp  NEWHORI		; 
; will print a new line and Decrease Horizantal 	
NEXTROW  	ADD R6, R6, #1		;
		LDI  R3, NEWLINE		;
		AND R0, R0, #0		; Reset it
		ADD R0, R3, R0		;
		OUT			;
		ADD R5, R5, #-1		;
		BRnzp NEWVERT		;
		

FINISH 	 HALT				;

ZERO .FILL x5000			; Will hold the Ascii to pring when you have a 0
