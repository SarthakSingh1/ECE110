;The point of this program is elarge a string using 2 different ascii  symbols
;The approach taken here was to first generate the 8 bits that were generated in Lab 12 and then save the address we started at
;Then once the 8 digits had been printed instead of printing a new line we incremented our address and then ran the program again
;This time we printed the first 8 bits of the next ascii character and so on
;Once we hit a NULL string we will go to our command earlier to print a new line and repeat for 10 cylces 

;Register Table
;R0 is what is used to output to the console 
;R1 is used both as out starting address and as a starting point for new lines
;R2 is what holds the data of the memory we are traversing 
;R3 Hold the starting address to check if we reached the end of a string
;R4 Is is a counter and a placeholder
;R5 is our vertical counter
;R6 is the memeory address that we load into R2
;R7 is used to check if M[R3] is null


.ORIG x3000 ; Start at x3000


		LDI R1, CHAR 		; Loads the hexidecimal value of CHAR into R1
		LD  R3, CHAR		; Load x5002 into LDI

; This code will take the hexicdecimal representation of CHAR and convert that into a value we can add to FONT_DATA
		AND R0, R0, #0 		; 
		ADD R0, R0, #4 		;
LOOP1		ADD R1, R1, R1 		;
		ADD R0, R0, #-1 	; 
		BRp LOOP1 		; 
; We are adding FONT_DATA to our R1 Calculated earlier
		LEA R6, FONT_DATA 	; 
		ADD R6, R6, R1 		; 
		LDR R2, R6, #0		; 
		AND R1, R1, #0		; 
		ADD R1, R6, #0		; This line will store our starting address in R1 forever

; Create a counter for the vertical columns 
		AND R5, R5, #0		;
		ADD R5, R5, #16      	; 
		BRnzp NEWVERT		;
		
;This is where we will be looping everytime we start part of a new letter
;It will do 8 bits at a time of each letter and loop   to LOOP0 when all letters are done

MAINLOOP 	LDR R4, R3, #0		;		
	
		AND R0, R0, #0 		; 
		ADD R0, R0, #4 		;
LOOP2		ADD R4, R4, R4		; We going to once again add FONT_DATA to 16*Hex version our letter
		ADD R0, R0, #-1 	; 
		BRp LOOP2 ; 
		
		LEA R6, FONT_DATA 	;
		ADD R6, R6, R4		;
		LD R4, RC		;
		ADD R6,R6,R4		;
		LDR R2, R6, #0		;
		
;Here we will be traversing all 8 bits of each letter

NEWVERT		ADD R5, R5, #0		; 	This will check to see if you hit 16 lines
		BRz FINISH		; 
		AND R4, R4, #0		; 
		ADD R4, R4, #8		;
		LDR R2, R6, #0		; 

; Here is the loop we use to pring 8 bits of a letter
NEWHORI		ADD R4, R4, #0 		; Print 1 or 0 algorithm(Line 46-71)
		BRz LOOP0		;  If we have finished all 8 bits we move to the loop which switches the letter
		AND R0, R0, #0		; 
		ADD R2, R2, #0		; 
		BRn PRINTONE 		; If the most signifigant bit is 1 then its negative
		LDI R0, ZERO		; 
		OUT			; 
		BRnzp  SHIFT		;  Preforms a left shit

	
PRINTONE	LDI R0, ONE 		; If  the value is negative we need  to print the character for one
		OUT			;

;We want to left shift the data to check if each bit is 1 or 0
SHIFT 		ADD R2, R2, R2 		;  
		ADD R4, R4, #-1		; 
		BRnzp  NEWHORI		; 

;This will print a new line when we finish the first line for each letter	
NEWLINE  	LD  R0, RC		; Everytime a new line is printed we want to increment our RC value to offset our FONT_DATA
		ADD R0, R0, #1		;
		ST R0,RC		;		
		AND R6, R6, #0		;
		ADD R1, R1, #1		;
		ADD R6, R6, R1		;
		LD  R0, LINE		;
		OUT			;
		ADD R5, R5, #-1		; Decrease vertical counter
		LD R3, CHAR		;
		BRnzp NEWVERT		;
;This is the part of code which will either move to the next 8 bits of a new letter or make a new line
LOOP0		AND R7, R7, #0		; 
		ADD R3, R3, #1		; 
		LDR R7, R3, #0		; 
		BRz  NEWLINE		;
		BRnzp MAINLOOP		; 




FINISH 	 HALT				;

